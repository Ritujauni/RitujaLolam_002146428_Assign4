    private void bttnviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnviewActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblpersondetails.getSelectedRow();
        
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a patient to see appointments and vital signs!");
            
        }
        else{
            DefaultTableModel pmodel = (DefaultTableModel) tblpersondetails.getModel();
            String patientid = pmodel.getValueAt(selectedRowIndex, 0).toString();
            
            DefaultTableModel amodel = (DefaultTableModel) tblappointments.getModel();
            amodel.setRowCount(0);
            
            for (Encounter e : encounterhistory.getEncounterHistory()){
                if(e.getPatientID()==patientid){
                    Object[] row = new Object[6];
                    row[0] = e;
                    row[1] = e.getAppointid();
                    row[2] = e.getDatetime();
                    row[3] = e.getBloodpress();
                    row[4] = e.getHeartrate();
                    row[5] = e.getResprate();
                    
            amodel.addRow(row);
                    
                }
            }
        }
        
        
        
        
    }//GEN-LAST:event_bttnviewActionPerformed

