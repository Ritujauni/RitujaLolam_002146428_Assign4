    private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblpersondetails.getModel();
        model.setRowCount(0);
        
        for(CarInfo ci : history.getHistory()){
            
            Object[] row = new Object[7];
            row[0] = ci;
            row[1] = ci.getName();
            row[2] = ci.getAddress();
            row[3] = ci.getAge();
            row[4] = ci.getGender();
            row[5] = ci.getCommunity();
            row[6] = ci.getCity();
            
            
        model.addRow(row);
        }
        DefaultTableModel pmodel = (DefaultTableModel) tblpatientdetails.getModel();
        pmodel.setRowCount(0);
        
        for(Patient p : patienthistory.getPatienthistory()){
            
            Object[] row = new Object[4];
            row[0] = p;
            row[1] = p.getInsuranceno();
            row[2] = p.getDiabetics();
            row[3] = p.getAllergy();
            
            
            
        pmodel.addRow(row);
        }
    }

   
    
    
    
