    private void bttnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnupdateActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblcardetails.getSelectedRow();
        
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select a row to Update");
            return;
        }
        
        if(txtname.getText().length()<=0 || txtaddress.getText().length()<=0 || txtage.getText().length()<=0 || 
                txtcommunity.getText().length()<=0 || txtgender.getText().length()<=0 || txtpersonid.getText().length()<=0 || 
                txtcity.getText().length()<=0 ){
            JOptionPane.showMessageDialog(this, "Any field is left blank!");
            
            
        }
        else{
            DefaultTableModel model = (DefaultTableModel) tblcardetails.getModel();
            CarInfo selectedCarDetails = (CarInfo) model.getValueAt(selectedRowIndex, 0);
        
            history.deleteCarDetails(selectedCarDetails);       
            populateTable();
        
            String id = txtpersonid.getText();
            String name = txtname.getText();
            String address = txtaddress.getText();
            int seats = Integer.parseInt(txtage.getText());
            String community = txtcommunity.getText();
            String gender = txtgender.getText();
            String city = txtcity.getText();
        
            CarInfo ci = history.addNewCarDetails();
            
            ci.setPersonid(id);
            ci.setName(name);
            ci.setAddress(address);
            ci.setAge(seats);
            ci.setCommunity(community);
            ci.setGender(gender);
            ci.setCity(city);
        
            JOptionPane.showMessageDialog(this, "Person Details Updated!Select Display again to view Updated Details");
            
            txtpersonid.setText("");
            txtname.setText("");
            txtaddress.setText("");
            txtage.setText("");
            txtcommunity.setText("");
            txtgender.setText("");
            txtcity.setText("");
        }
        
        
    }//GEN-LAST:event_bttnupdateActionPerformed

